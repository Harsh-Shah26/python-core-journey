# Python While Loop Practice

# 1. Print numbers from 1 to 5

i = 1

while i <= 5:
    print(i)
    i += 1

# 2. Print numbers from 5 to 1

i = 5

while i >= 1:
    print(i)
    i -= 1

# 3. Print even numbers from 2 to 10

i = 2

while i <= 10:
    print(i)
    i += 2

# 4. Print odd numbers from 1 to 9

i = 1

while i <= 9:
    print(i)
    i += 2

# 5. Print your name 5 times

count = 1

while count <= 5:
    print("Harsh")
    count += 1